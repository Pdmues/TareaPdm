package sv.ues.fia.bitacora;

import java.util.Date;

public class Bitacora {

	private int idbitacora;
	private String ntg;
	private String quien;
	private String lugar;
	private int etapadesarrollada;
	private String horainicio;
	private String horafin;
	
	
	public Bitacora() {
	}
	public int getIdbitacora() {
		return idbitacora;
	}
	public void setIdbitacora(int idbitacora) {
		this.idbitacora = idbitacora;
	}
	public String getNtg() {
		return ntg;
	}
	public void setNtg(String ntg) {
		this.ntg = ntg;
	}
	public String getQuien() {
		return quien;
	}
	public void setQuien(String quien) {
		this.quien = quien;
	}
	public String getLugar() {
		return lugar;
	}
	public void setLugar(String lugar) {
		this.lugar = lugar;
	}
	public int getEtapadesarrollada() {
		return etapadesarrollada;
	}
	public void setEtapadesarrollada(int etapadesarrollada) {
		this.etapadesarrollada = etapadesarrollada;
	}
	public String getHorainicio() {
		return horainicio;
	}
	public void setHorainicio(String horainicio) {
		this.horainicio = horainicio;
	}
	public String getHorafin() {
		return horafin;
	}
	public void setHorafin(String horafin) {
		this.horafin = horafin;
	}
	

	
	
		
}
